void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	uint8_t index;
	
  if(hadc==(&hadc1))
  {
		HAL_ADC_Stop_DMA(&hadc1);
		for(index=0;index<16;index++)
    {
			AD_ConvertVal_Final[index]=AD_ConvertVal[index];
			AD_TransValue[2*index]=AD_ConvertVal[index]>>8;
			AD_TransValue[2*index+1]=AD_ConvertVal[index]&0xFF;
		}
		HAL_ADC_Start_DMA(&hadc1, (uint32_t*)&AD_ConvertVal, 16);
  }
	
	  if(hadc==(&hadc3))
  {
			HAL_ADC_Stop_DMA(&hadc3);
		for(index=0;index<8;index++)
    {
			AD_ConvertVal_Final[index+16]=AD_ConvertVal[index];
			AD_TransValue[2*index+32]=AD3_ConvertVal[index]>>8;
			AD_TransValue[2*index+1+32]=AD3_ConvertVal[index]&0xFF;
		}
		HAL_ADC_Start_DMA(&hadc3, (uint32_t*)&AD3_ConvertVal, 8);
  }

	
}
